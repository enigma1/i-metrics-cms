    <h2>Creating a new page</h2>
	<ol>
	  <li>At the bottom of the departments list click the <b>Insert</b> button</li>
	  <li>Fill In the Department details on the right</li>
	  <li>Click Insert to create the new department.</li>
	</ol>
	<p>There are several entries you need to fill-up in order to create a new helpdesk departmnent.<br />
	You must have the email account already created from your host's cPanel before adding it to the helpdesk</p>
	<ul>
	  <li>Department: is the department name, <b>(eg: sales)</b></li>
	  <li>E-Mail Address: the e-mail address for the mailbox. <b>(eg: joe@example.com)</b></li>
	  <li>Password: the password for the email to download messages.</li>
	  <li>Name: The mailbox name will be used by the helpdesk to download e-mails. It is also used for the mail server connect so if you are unsure, use the full emai(eg: joe@example.com)</li>
	</ul>
    <h2>Edit existing departments</h2>
	<ol>
	  <li>Select the edit icon to directly edit a department or select the department first using the information icon, then click the edit button to the right</li>
	  <li>Change the Department details on the right</li>
	  <li>Click Update to save the changes for this helpdesk department, click cancel to abort changes</li>
	</ol>
	<p>Here only the relevant edit options are listed, for the general option details of a department see the creaating a new department section above.</p>
	<ul>
	  <li>Set as default: Tick to have this department as the default one to receive/sent e-mail messages or when no department is selected/specified.</li>
	  <li>Show on Front: Whether the department will show on the web-front (eg: with the contact us page)</li>
	  <li>Mailbox Receives: Whether this mailbox is included on global email sync. Otherwise it will only retrieve e-mails upon request</li>
	  <li>Read Only: When checked, the department mailbox will not delete e-mails from the server during download, by default messages are downloaded stored in the database and the mailbox is cleaned up.</li>
	</ul>
