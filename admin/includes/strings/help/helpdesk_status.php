  <div>
    <p>Helpdesk status entries are used for the incoming emails. They signify the status of a ticket such as closed, opened, pending etc.<br />
    Tickets can be filtered by statuses so the administrator can locate the opened tickets by clicking a button.</p>
    <h2>Creating a new Status</h2>
	<ol>
	  <li>At the bottom of the departments list click the <b>Insert</b> button</li>
	  <li>Fill In the Status details on the right</li>
	  <li>Click Confirm to create the new Status.</li>
	</ol>
	<p>The default checkbox applies to new e-mail messages. So if the Pending status is set as default any incoming email will have the pending status set.</p>
    <h2>Edit an existing Status</h2>
	<ol>
	  <li>Select the edit icon to directly edit a status or select the status first using the information icon, then click the edit button to the right</li>
	  <li>Change the Status details on the right</li>
	  <li>Click Update to save the changes for this Status entry, click cancel to abort changes</li>
	</ol>
    <h2>Delete a Status Entry</h2>
	<ol>
	  <li>Select the cross/delete icon to directly delete a status or select the status first using the information icon, then click the delete button to the right</li>
	  <li>Click the confirm button to delete the status entry</li>
	</ol>
  </div>